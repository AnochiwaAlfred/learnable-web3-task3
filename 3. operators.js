// LOGICAL OPERATORS

// true && false   // logical AND returns the second value if first is true
// false || true   // logical OR returns the first value if it's true 
// !true          // NOT operator negates the boolean value 
// 0 ?? 3        // Nullish coalescing: returns the number on the left (if it is not null), else it returns the one on the right


// COMPARISON OPERATORS

// x == y        // equal to (loose equality). It converts values if necessary
// x === y       // strictly equal to (strict equality). Doesn’t convert values
// x != y        // not equal to (loose inequality). Converts values if necessary
// x !== y       // not strictly equal to (strict inequality). Doesn’t convert values
// x > y         // greater than
// x < y         // less than
// x >= y        // greater than or equal to
// x <= y        // less than or equal to

